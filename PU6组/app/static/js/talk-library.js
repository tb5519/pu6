function getTrackCategory(track) {
  const category = String(track?.分类 || "").trim();
  if (removedTalkCategories.includes(category)) return "";
  return talkCategories.includes(category) ? category : defaultTalkCategory;
}

function isRemovedTalkTrack(track) {
  return removedTalkCategories.includes(track?.分类);
}

function getRenewalTrackType(track) {
  const type = String(track?.类型 || track?.type || "").trim();
  return renewalTalkTypes.includes(type) ? type : defaultRenewalTalkType;
}

function normalizeTrack(track) {
  const normalized = {
    ...track,
    分类: getTrackCategory(track),
  };
  if (normalized.分类 === defaultTalkCategory) {
    normalized.类型 = getRenewalTrackType(track);
  }
  return normalized;
}

function normalizeTrackIdentityText(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/\s+/g, "")
    .replace(/[，。！？、,.!?;；"“”'‘’()（）【】[\]]/g, "");
}

function trackIdentity(track) {
  const normalized = normalizeTrack(track);
  return [
    normalized.分类,
    normalized.类型,
    normalized.场景,
    normalized.问题示例,
    normalized.标准话术,
  ].map(normalizeTrackIdentityText).join("|");
}

function storedTalkTracks() {
  try {
    return JSON.parse(localStorage.getItem(talkStorageKey) || "null");
  } catch (_error) {
    return null;
  }
}

function loadTalkTracks() {
  const storedTracks = storedTalkTracks();
  const storedVersion = localStorage.getItem(talkStorageVersionKey);

  if (!Array.isArray(storedTracks)) {
    localStorage.setItem(talkStorageVersionKey, talkStorageVersion);
    return seededTalkTracks.map(normalizeTrack);
  }

  const normalizedStored = storedTracks
    .filter((track) => !isRemovedTalkTrack(track))
    .map(normalizeTrack);
  if (storedVersion === talkStorageVersion) return normalizedStored;

  const seen = new Set(normalizedStored.map(trackIdentity));
  const mergedTracks = [...normalizedStored];
  seededTalkTracks.map(normalizeTrack).forEach((track) => {
    const identity = trackIdentity(track);
    if (seen.has(identity)) return;
    seen.add(identity);
    mergedTracks.push(track);
  });

  localStorage.setItem(talkStorageKey, JSON.stringify(mergedTracks));
  localStorage.setItem(talkStorageVersionKey, talkStorageVersion);
  return mergedTracks;
}

