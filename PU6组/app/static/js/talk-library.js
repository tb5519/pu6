function extractLearningSection(text, label) {
  const source = String(text || "");
  const marker = `${label}:`;
  const startIndex = source.indexOf(marker);
  if (startIndex === -1) return "";

  const contentStart = startIndex + marker.length;
  const nextIndexes = learningGuideAllLabels
    .filter((item) => item !== label)
    .flatMap((item) => [`\n\n${item}:`, `\n${item}:`])
    .map((item) => source.indexOf(item, contentStart))
    .filter((index) => index > contentStart);
  const endIndex = nextIndexes.length ? Math.min(...nextIndexes) : source.length;
  return source.slice(contentStart, endIndex).trim();
}

