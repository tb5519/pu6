function splitKeywords(text) {
  return String(text || "")
    .split(/[,，、\s]+/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function normalizeText(text) {
  return String(text || "")
    .toLowerCase()
    .replace(/\s+/g, "")
    .replace(/[，。！？、,.!?;；:"“”'‘’()（）【】\[\]]/g, "");
}

function bigrams(text) {
  const clean = normalizeText(text);
  const output = new Set();
  for (let index = 0; index < clean.length - 1; index += 1) {
    output.add(clean.slice(index, index + 2));
  }
  return output;
}

function similarity(a, b) {
  const left = bigrams(a);
  const right = bigrams(b);
  if (!left.size || !right.size) return 0;

  let hits = 0;
  left.forEach((item) => {
    if (right.has(item)) hits += 1;
  });
  return hits / Math.max(left.size, right.size);
}

function expandedKeywordHits(question, keywords) {
  const normalizedQuestion = normalizeText(question);
  const hits = [];

  keywords.forEach((keyword) => {
    const normalizedKeyword = normalizeText(keyword);
    if (normalizedKeyword && normalizedQuestion.includes(normalizedKeyword)) {
      hits.push(keyword);
    }
  });

  synonymGroups.forEach((group) => {
    const hasQuestionSide = group.some((word) => normalizedQuestion.includes(normalizeText(word)));
    if (!hasQuestionSide) return;

    const matched = keywords.find((keyword) => group.includes(keyword));
    if (matched && !hits.includes(matched)) hits.push(matched);
  });

  return hits;
}

function matchTracks(question) {
  return currentCategoryTracks()
    .map(({ track }) => {
      const keywords = splitKeywords(track.关键词);
      const hits = expandedKeywordHits(question, keywords);
      const keywordScore = hits.length * 30;
      const sceneScore = similarity(question, track.场景) * 15;
      const exampleScore = similarity(question, track.问题示例) * 35;
      const talkScore = similarity(question, track.标准话术) * 10;
      const priority = Number(track.优先级 || 0);
      const score = keywordScore + sceneScore + exampleScore + talkScore + priority;
      return { track, hits, score };
    })
    .filter((item) => item.score > 3)
    .sort((a, b) => b.score - a.score)
    .slice(0, 5);
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let quote = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];
    if (quote && char === '"' && next === '"') {
      cell += '"';
      index += 1;
    } else if (char === '"') {
      quote = !quote;
    } else if (!quote && char === ",") {
      row.push(cell);
      cell = "";
    } else if (!quote && (char === "\n" || char === "\r")) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(cell);
      if (row.some((value) => value.trim() !== "")) rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }

  row.push(cell);
  if (row.some((value) => value.trim() !== "")) rows.push(row);
  if (!rows.length) return [];

  const headers = rows[0].map((item) => item.trim());
  return rows
    .slice(1)
    .map((values) => {
      const item = {};
      headers.forEach((header, index) => {
        item[header] = values[index] || "";
      });
      return item;
    })
    .filter((item) => item.标准话术 || item.talktrack || item.answer);
}

function escapeHtml(value) {
  return String(value || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function renderLibrary() {
  if (!canManageTalk || !tl("libraryStatus") || !tl("library")) return;

  const tracks = currentCategoryTracks();
  tl("libraryStatus").textContent = `${tracks.length} 条话术`;

  if (!tracks.length) {
    tl("library").innerHTML = `<div class="empty-state compact-empty">当前分类暂无话术，可以新增或导入 CSV。</div>`;
    return;
  }

  tl("library").innerHTML = tracks
    .map(({ track, index }) => `
      <article class="talk-library-item">
        <strong>${escapeHtml(track.场景 || "未分类")}</strong>
        <span>${escapeHtml(track.关键词 || "")}</span>
        <button type="button" data-delete-talk="${index}">删除</button>
      </article>
    `)
    .join("");

  document.querySelectorAll("[data-delete-talk]").forEach((button) => {
    button.addEventListener("click", () => {
      talkState.tracks.splice(Number(button.dataset.deleteTalk), 1);
      saveTalkTracks();
      renderLibrary();
      renderResults();
    });
  });
}

async function copyText(text, button) {
  try {
    await navigator.clipboard.writeText(text);
  } catch {
    const fallback = document.createElement("textarea");
    fallback.value = text;
    document.body.appendChild(fallback);
    fallback.select();
    document.execCommand("copy");
    fallback.remove();
  }

  button.textContent = "已复制";
  setTimeout(() => {
    button.textContent = "复制话术";
  }, 1200);
}

